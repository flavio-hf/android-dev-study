fun main(){
    // data types max values calls (when you need to know how big a variable can be)
    println(Int.MAX_VALUE)
    println(Byte.MAX_VALUE)
    println(Double.MAX_VALUE)
    println(Float.MAX_VALUE)

    // data types can be easily converted with dot notation
    var number = 1
    println(number)
    // convert to float
    println(number.toFloat())
}